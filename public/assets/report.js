const esc = (v) => String(v ?? "").replaceAll("&", "&amp;").replaceAll("<", "&lt;").replaceAll(">", "&gt;").replaceAll('"', "&quot;");

async function main() {
  const campaignId = new URLSearchParams(window.location.search).get("campaign") ?? "";
  const response = await fetch(`/api/campaign${campaignId ? `?id=${encodeURIComponent(campaignId)}` : ""}`);
  const data = await response.json();
  const { campaign, test_cards: cards, findings, repair_tasks: repairs, persistence, run_stats: runStats } = data;

  const executed = new Set(runStats?.executedCardIds ?? []);
  const passed = cards.filter((c) => c.status === "passed").length;
  const failed = cards.filter((c) => c.status === "failed").length;
  const blocked = cards.filter((c) => c.status === "blocked");
  const pending = cards.filter((c) => c.status === "ready").length;

  const rows = cards
    .map(
      (c) => `
      <tr>
        <td><span class="pill ${esc(c.status)}">${esc(c.status)}</span></td>
        <td><b>${esc(c.id)}</b> — ${esc(c.title)}${executed.has(c.id) ? "" : c.status === "blocked" ? "" : " <em style='color:var(--faint)'>(not executed)</em>"}</td>
        <td>${esc(c.category)}</td>
        <td>${esc(c.risk)}</td>
        <td>${esc(c.acceptanceCriteria || "—")}</td>
      </tr>`,
    )
    .join("");

  const findingBlocks = findings.length
    ? findings
        .map(
          (f) => `
        <div class="finding">
          <h3>${esc(f.severity.toUpperCase())} · ${esc(f.type.replaceAll("_", " "))} — ${esc(f.title)}</h3>
          <p>${esc(f.summary)}</p>
          <div class="evidence">Evidence: ${f.evidenceRefs?.length ? f.evidenceRefs.map(esc).join(" · ") : "none recorded"}</div>
        </div>`,
        )
        .join("")
    : `<p class="sub">No findings. All executed checks passed.</p>`;

  const blockedBlock = blocked.length
    ? `<div class="note"><b>Declared coverage gaps (${blocked.length}):</b> ${blocked.map((c) => esc(c.title)).join(" · ")}. These are reported as blocked, not silently skipped — resolving the named prerequisites and re-running closes them.</div>`
    : "";

  const provenance = persistence?.mode === "postgres"
    ? `Results loaded from durable campaign storage. Readiness is computed from executed results only.`
    : `DEMO MODE: seeded sample data — not a real audit. Configure persistence to record real campaigns.`;

  document.getElementById("sheet").innerHTML = `
    <div class="masthead">
      <div class="brand"><div class="brand-mark">F</div><div><b>Fusion LaunchAudit</b><div style="color:var(--faint);font-size:12px">Launch Audit Report</div></div></div>
      <div class="meta">${esc(new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" }))}<br/>Campaign ${esc(campaign.id)}<br/>Target: ${esc(campaign.appUrl)}</div>
    </div>
    <div class="headline">
      <div class="score">${esc(campaign.readinessScore)}<small>/100</small></div>
      <div><h1>${esc(campaign.name)}</h1><div class="sub">${esc(provenance)}</div></div>
    </div>
    <div class="tiles">
      <div class="tile"><b>Passed</b><strong>${passed}</strong></div>
      <div class="tile"><b>Failed</b><strong>${failed}</strong></div>
      <div class="tile"><b>Blocked (declared)</b><strong>${blocked.length}</strong></div>
      <div class="tile"><b>Evidence artifacts</b><strong>${runStats?.artifacts ?? 0}</strong></div>
    </div>
    ${pending > 0 ? `<div class="note">${pending} card(s) generated but not yet executed — excluded from the readiness score.</div>` : ""}
    <h2>Test results</h2>
    <table><thead><tr><th>Status</th><th>Check</th><th>Category</th><th>Risk</th><th>Acceptance criteria</th></tr></thead><tbody>${rows}</tbody></table>
    <h2>Findings</h2>
    ${findingBlocks}
    ${blockedBlock}
    ${repairs.length ? `<h2>Repair packets</h2>` + repairs.map((r) => `
      <div class="finding" style="border-left-color:var(--blue)">
        <h3>${esc(r.title)}</h3>
        <p>${esc(r.why_it_matters)}</p>
        <div class="evidence">Verify: ${esc(r.verification_command)}</div>
      </div>`).join("") : ""}
    <div class="foot"><span>Generated by Fusion LaunchAudit — evidence-gated launch QA</span><span>fusiondataco.com</span></div>
  `;
}

main().catch((error) => {
  document.getElementById("sheet").innerHTML = `<div class="loading">Report failed to load: ${esc(error.message)}</div>`;
});
